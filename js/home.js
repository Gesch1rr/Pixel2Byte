  function drawBitmap() {
    const id = ctx.createImageData(W, H);
    for (let i = 0; i < W * H; i++) {
      const v = bitmap[i] ? 0 : 255;
      id.data[i*4]   = v;
      id.data[i*4+1] = v;
      id.data[i*4+2] = v;
      id.data[i*4+3] = 255;
    }
    ctx.putImageData(id, 0, 0);
  }

  function buildOutput() {
    const bpr = Math.ceil(W / 8);
    const bytes = [];
    for (let r = 0; r < H; r++) {
      for (let b = 0; b < bpr; b++) {
        let byte = 0;
        for (let bit = 0; bit < 8; bit++) {
          const col = b * 8 + bit;
          if (col < W && bitmap[r * W + col]) byte |= (0x80 >> bit);
        }
        bytes.push(byte);
      }
    }

    bytesEl.textContent = bytes.length;

    const lines = ['const unsigned char alien[] PROGMEM = {'];
    for (let row = 0; row < 6; row++) {
      const chunk = bytes.slice(row * bpr, (row + 1) * bpr);
      lines.push('  ' + chunk.map(b => '0x' + b.toString(16).toUpperCase().padStart(2,'0')).join(', ') + ',');
    }
    lines.push('  // ... ' + (bytes.length - 6 * bpr) + ' more bytes');
    lines.push('};');

    codeEl.textContent = '';
    let i = 0;
    const full = lines.join('\n');
    const interval = setInterval(() => {
      codeEl.textContent = full.slice(0, i);
      i += 3;
      if (i > full.length) {
        codeEl.textContent = full;
        clearInterval(interval);
      }
    }, 16);
  }

  drawBitmap();
  buildOutput();
